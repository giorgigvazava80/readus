interface I18nContextValue {
  language: AppLanguage;
  setLanguage: (next: AppLanguage) => void;
  toggleLanguage: () => void;
  t: (key: string, fallback?: string) => string;
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

function resolveInitialLanguage(): AppLanguage {
  if (typeof window === "undefined") {
    return "ka";
  }

  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === "en" || stored === "ka") {
      return stored;
    }
  } catch {
    // Ignore storage errors and continue with fallback logic.
  }

  const browser = window.navigator.language.toLowerCase();
  if (browser.startsWith("ka")) {
    return "ka";
  }

  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(() => resolveInitialLanguage());

  const setLanguage = (next: AppLanguage) => {
    setLanguageState(next);
    try {
      localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // Ignore storage errors.
    }
  };

  const toggleLanguage = () => {
    setLanguage(language === "en" ? "ka" : "en");
  };

  useEffect(() => {
    const dict = dictionaries[language];
    document.documentElement.lang = language;
    document.title = isAdminAppHost()
      ? dict["title.admin"] || "Readus Admin"
      : dict["title.user"] || "Readus";
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, fallback?: string) => {
      const dict = dictionaries[language];
      return dict[key] || fallback || key;
    };

    return {
      language,
      setLanguage,
      toggleLanguage,
      t,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}



